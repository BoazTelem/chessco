/**
 * Master directory of every FIDE-recognized national federation (Phase 0 W7
 * expansion, 2026-05-14).
 *
 * Source of truth: FIDE handbook "F. Federations Directory" + FIDE continental
 * federation membership (ECU, FIDE-Americas, ACF, ACC, FOC). Coverage is the
 * ~199 member federations as of 2024–2026 council.
 *
 * This const is the SEED for the `federations` table. The DB row is the
 * runtime source of truth — `rating_list_url`, `scrape_strategy`, `active`
 * may drift over time via small migrations as parsers ship. We update this
 * const opportunistically.
 *
 * Notable FIDE/ISO mismatches preserved here:
 *   FIDE ENG/SCO/WLS → ISO GB sub-countries (no unique alpha-3)
 *   FIDE GER         → ISO DE / DEU
 *   FIDE NED         → ISO NL / NLD
 *   FIDE SUI         → ISO CH / CHE
 *   FIDE TPE         → ISO TW / TWN (Chinese Taipei)
 *   FIDE IRI         → ISO IR / IRN
 *   FIDE INA         → ISO ID / IDN
 *   FIDE FAI         → ISO FO / FRO (Faroe Islands)
 *   FIDE GUE/JCI/IOM → ISO Crown Dependencies (no unique alpha-3 in some lists)
 *
 * Continent classification follows FIDE Continental Federation:
 *   EU = European Chess Union (ECU)
 *   NA = FIDE-Americas (north) — Mexico is grouped here per FIDE
 *   SA = FIDE-Americas (south)
 *   AS = Asian Chess Federation (ACF)
 *   AF = African Chess Confederation (ACC)
 *   OC = Oceania Chess Confederation (FOC)
 */

export type ScrapeStrategy =
  | 'dump' //         download zipped/CSV/XML — cluster A
  | 'fetch-html' //   fetch() + cheerio — cluster B
  | 'aspnet' //       ASP.NET ViewState postback — cluster C
  | 'spa' //          Playwright SPA render — cluster D
  | 'api' //          JSON REST endpoint — cluster E
  | 'cloudflare' //   Playwright behind Cloudflare — cluster F
  | 'placeholder'; // no native list yet — cluster G

export type Continent = 'AF' | 'AS' | 'EU' | 'NA' | 'OC' | 'SA';

export type SyncCadence = 'monthly' | 'quarterly' | 'semi_annual' | 'manual';

export type RatingListFormat = 'xml' | 'csv' | 'json' | 'html';

export interface FederationDirectoryEntry {
  /** FIDE 3-letter code (primary key in `federations.id`). */
  code: string;
  /** Authoritative full name in English. */
  name: string;
  /** ISO 3166-1 alpha-2. null only for FIDE itself. */
  iso2: string | null;
  /** ISO 3166-1 alpha-3. null for sub-country federations (ENG/SCO/WLS). */
  iso3: string | null;
  continent: Continent | null;
  /** Best-known public rating-list URL. May rot — verify when building parser. */
  ratingListUrl: string | null;
  /** Source format (drives the parser file). null where unknown. */
  ratingListFormat: RatingListFormat | null;
  /** Cluster hint — drives wave bucketing and helper choice. */
  scrapeStrategy: ScrapeStrategy;
  /** Coarse order-of-magnitude — used to size batches + show on roster header. */
  estPlayerCount: number | null;
  /** How often the federation publishes a fresh list. */
  syncCadence: SyncCadence;
  /** true only once the bespoke worker ships; default false. */
  active: boolean;
  /** Freeform notes: bot wall caveats, URL flux warnings, paid-membership gates. */
  notes?: string;
}

