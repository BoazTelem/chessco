/** O(1) lookup. */
export const FEDERATIONS_BY_CODE: Record<string, FederationDirectoryEntry> = Object.fromEntries(
  FEDERATION_DIRECTORY.map((f) => [f.code, f]),
);

/** Total federation count — used in tests/assertions. */
export const FEDERATION_COUNT = FEDERATION_DIRECTORY.length;

/** Group by continent for UI dropdowns. */
export function groupByContinent(): Record<string, FederationDirectoryEntry[]> {
  const groups: Record<string, FederationDirectoryEntry[]> = {};
  for (const f of FEDERATION_DIRECTORY) {
    const key = f.continent ?? 'INT';
    (groups[key] ??= []).push(f);
  }
  return groups;
}

/**
 * Map FIDE alpha-3 codes that we don't use as PKs back to our canonical code.
 * Used by the `/scout/federation/[code]` route to 301-redirect FIDE-style URLs
 * (e.g. `/scout/federation/USA`) to the canonical row (`/scout/federation/USCF`).
 */
export const FIDE_CODE_ALIASES: Record<string, string> = {
  USA: 'USCF',
  ENG: 'ECF',
  GER: 'DSB',
  FRA: 'FFE',
  ITA: 'FSI',
  ISR: 'ICF',
};

/**
 * Return the canonical federation code for any of:
 *   - an existing PK in `federations.id` (passthrough)
 *   - a FIDE alpha-3 alias (USA → USCF, ENG → ECF, …)
 *   - mixed-case input (uppercase-normalized)
 */
export function canonicalFederationCode(input: string): string | null {
  const up = input.trim().toUpperCase();
  if (FEDERATIONS_BY_CODE[up]) return up;
  const aliased = FIDE_CODE_ALIASES[up];
  if (aliased && FEDERATIONS_BY_CODE[aliased]) return aliased;
  return null;
}
